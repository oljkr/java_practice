package ch04;

//클래스 = 여러가지 특징(상태)을 가지고 있다.
// int -> 한가지의 상태만 가질 수 있다.
// String -> 한가지의 상태만 가질 수 있다.
public class Dog {
	int age = 20; //변경 가능
	String name = "토토"; //변경 가능
	String color = "하얀색"; //변경 가능
	final String type = "말티즈"; // 변경 불가능
}

